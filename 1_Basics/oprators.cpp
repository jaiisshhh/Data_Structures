#include<iostream>
using namespace std;

int main(){
    
    int a = 10;
    int b = 20;

    //Arithmetic Oprators 
    cout<< a+b <<endl; //Additon
    cout<< a-b <<endl; //Substractin
    cout<< a*b <<endl; //Multiplication
    cout<< a/b <<endl; //Division
    cout<< a%b <<endl; //Modulas 

    //Relational Operatior 
    cout<<(a==b)<<endl;  
    cout<<(a>b)<<endl;
    cout<<(a>=b)<<endl;
    cout<<(a<b)<<endl;
    cout<<(a<=b)<<endl;
    cout<<(a!=b)<<endl;

    return 0;
}